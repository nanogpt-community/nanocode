import { TextAttributes, RGBA } from "@opentui/core"
import { For, type JSX } from "solid-js"
import { useTheme, tint } from "@tui/context/theme"

const LOGO_LEFT = [`                   `, `‚Ėą‚ĖÄ‚ĖÄ‚ĖĄ ‚Ėą‚ĖÄ‚ĖÄ‚Ėą ‚Ėą‚ĖÄ‚ĖÄ‚ĖĄ ‚Ėą‚ĖÄ‚ĖÄ‚Ėą`, `‚Ėą‚ĖĎ‚ĖĎ‚Ėą ‚Ėą‚ĖÄ‚ĖÄ‚Ėą ‚Ėą‚ĖĎ‚ĖĎ‚Ėą ‚Ėą‚ĖĎ‚ĖĎ‚Ėą`, `‚ĖÄ  ‚ĖÄ ‚ĖÄ  ‚ĖÄ ‚ĖÄ  ‚ĖÄ ‚ĖÄ‚ĖÄ‚ĖÄ‚ĖÄ`]

const LOGO_RIGHT = [`                   `, `‚Ėą‚ĖÄ‚ĖÄ‚ĖÄ ‚Ėą‚ĖÄ‚ĖÄ‚Ėą ‚Ėą‚ĖÄ‚ĖÄ‚ĖĄ ‚Ėą‚ĖÄ‚ĖÄ‚ĖÄ`, `‚Ėą‚ĖĎ‚ĖĎ‚ĖĎ ‚Ėą‚ĖĎ‚ĖĎ‚Ėą ‚Ėą‚ĖĎ‚ĖĎ‚Ėą ‚Ėą‚ĖÄ‚ĖÄ‚ĖÄ`, `‚ĖÄ‚ĖÄ‚ĖÄ‚ĖÄ ‚ĖÄ‚ĖÄ‚ĖÄ‚ĖÄ ‚ĖÄ‚ĖÄ‚ĖÄ‚ĖÄ ‚ĖÄ‚ĖÄ‚ĖÄ‚ĖÄ`]

const SHADOW_MARKER = /[_^~]/

export function Logo() {
  const { theme } = useTheme()

  const renderLine = (line: string, fg: RGBA, bold: boolean): JSX.Element[] => {
    const shadow = tint(theme.background, fg, 0.25)
    const attrs = bold ? TextAttributes.BOLD : undefined
    const elements: JSX.Element[] = []
    let i = 0

    while (i < line.length) {
      const rest = line.slice(i)
      const markerIndex = rest.search(SHADOW_MARKER)

      if (markerIndex === -1) {
        elements.push(
          <text fg={fg} attributes={attrs} selectable={false}>
            {rest}
          </text>,
        )
        break
      }

      if (markerIndex > 0) {
        elements.push(
          <text fg={fg} attributes={attrs} selectable={false}>
            {rest.slice(0, markerIndex)}
          </text>,
        )
      }

      const marker = rest[markerIndex]
      switch (marker) {
        case "_":
          elements.push(
            <text fg={fg} bg={shadow} attributes={attrs} selectable={false}>
              {" "}
            </text>,
          )
          break
        case "^":
          elements.push(
            <text fg={fg} bg={shadow} attributes={attrs} selectable={false}>
              ‚ĖÄ
            </text>,
          )
          break
        case "~":
          elements.push(
            <text fg={shadow} attributes={attrs} selectable={false}>
              ‚ĖÄ
            </text>,
          )
          break
      }

      i += markerIndex + 1
    }

    return elements
  }

  return (
    <box>
      <For each={LOGO_LEFT}>
        {(line, index) => (
          <box flexDirection="row" gap={1}>
            <box flexDirection="row">{renderLine(line, theme.textMuted, false)}</box>
            <box flexDirection="row">{renderLine(LOGO_RIGHT[index()], theme.text, true)}</box>
          </box>
        )}
      </For>
    </box>
  )
}
